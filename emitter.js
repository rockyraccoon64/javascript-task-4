'use strict';

/**
 * Сделано задание на звездочку
 * Реализованы методы several и through
 */
const isStar = false;

/**
 * Возвращает новый emitter
 * @returns {Object}
 */
function getEmitter() {

    let subscriptions = [];

    function isSuperevent(superevent, event) {
        return event.indexOf(superevent) === 0 &&
            (event.length === superevent.length || event[superevent.length] === '.');
    }

    return {

        /**
         * Подписаться на событие
         * @param {String} event
         * @param {Object} context
         * @param {Function} handler
         * @returns {Object} this
         */
        on: function (event, context, handler) {

            subscriptions.push({
                event,
                context,
                handler
            });

            return this;
        },

        /**
         * Отписаться от события
         * @param {String} event
         * @param {Object} context
         * @returns {Object} this
         */
        off: function (event, context) {

            subscriptions = subscriptions.filter((subscription) => {
                return subscription.context !== context ||
                    !isSuperevent(event, subscription.event);
            });

            return this;
        },

        /**
         * Уведомить о событии
         * @param {String} event
         * @returns {Object} this
         */
        emit: function (event) {

            const suitableSubscriptions = subscriptions.filter((subscription) => {
                return isSuperevent(subscription.event, event);
            });

            suitableSubscriptions.sort((a, b) => {
                return a.event.split('.').length < b.event.split('.').length;
            });

            for (let subscription of suitableSubscriptions) {
                subscription.handler.call(subscription.context);
            }

            return this;
        },

        /**
         * Подписаться на событие с ограничением по количеству полученных уведомлений
         * @star
         * @param {String} event
         * @param {Object} context
         * @param {Function} handler
         * @param {Number} times – сколько раз получить уведомление
         */
        several: function (event, context, handler, times) {
            console.info(event, context, handler, times);
        },

        /**
         * Подписаться на событие с ограничением по частоте получения уведомлений
         * @star
         * @param {String} event
         * @param {Object} context
         * @param {Function} handler
         * @param {Number} frequency – как часто уведомлять
         */
        through: function (event, context, handler, frequency) {
            console.info(event, context, handler, frequency);
        }
    };
}

module.exports = {
    getEmitter,

    isStar
};
